/**
 * virtio_gpu_init — Probe VirtIO MMIO slots, find GPU (ID 16), initialise it.
 * Must be called early in boot before the first hal_serial_write() calls that
 * should appear on screen (or at least before they matter visually).
 */
void virtio_gpu_init(void)
{
    /* 1. Find the GPU device by probing MMIO slots */
    uintptr_t base = 0;
    for (int i = 0; i < VIRT_MMIO_COUNT; i++) {
        uintptr_t candidate = (uintptr_t)(VIRT_MMIO_BASE + (uint64_t)i * VIRT_MMIO_STRIDE);
        uint32_t magic  = mmio_r32(candidate, VIRTIO_MMIO_MAGIC_VALUE);
        uint32_t dev_id = mmio_r32(candidate, VIRTIO_MMIO_DEVICE_ID);
        if (magic == 0x74726976u && dev_id == VIRTIO_ID_GPU) {
            base = candidate;
            break;
        }
    }
    if (!base) return;  /* no GPU found, stay silent */
    s_gpu_base = base;

    /* 2. Reset device */
    mmio_w32(base, VIRTIO_MMIO_STATUS, 0);

    /* 3. Acknowledge + Driver bits */
    uint32_t status = VIRTIO_STATUS_ACKNOWLEDGE | VIRTIO_STATUS_DRIVER;
    mmio_w32(base, VIRTIO_MMIO_STATUS, status);

    /* 4. Feature negotiation — we don't need any advanced features */
    mmio_w32(base, VIRTIO_MMIO_DEVICE_FEATURES_SEL, 0);
    mmio_w32(base, VIRTIO_MMIO_DRIVER_FEATURES_SEL, 0);
    mmio_w32(base, VIRTIO_MMIO_DRIVER_FEATURES, 0);

    /* 5. Features OK */
    status |= VIRTIO_STATUS_FEATURES_OK;
    mmio_w32(base, VIRTIO_MMIO_STATUS, status);
    /* (In a real driver we'd re-read and verify FEATURES_OK; skip for simplicity) */

    /* 6. Setup controlq (queue 0) */
    mmio_w32(base, VIRTIO_MMIO_QUEUE_SEL, CONTROLQ);
    uint32_t q_max = mmio_r32(base, VIRTIO_MMIO_QUEUE_NUM_MAX);
    if (q_max == 0) return;
    uint32_t q_num = (VQ_SIZE < (int)q_max) ? VQ_SIZE : (int)q_max;
    mmio_w32(base, VIRTIO_MMIO_QUEUE_NUM, q_num);

    uint64_t desc_pa  = (uint64_t)(uintptr_t)s_desc;
    uint64_t avail_pa = (uint64_t)(uintptr_t)&s_avail;
    uint64_t used_pa  = (uint64_t)(uintptr_t)&s_used;

    mmio_w32(base, VIRTIO_MMIO_QUEUE_DESC_LOW,  (uint32_t)(desc_pa));
    mmio_w32(base, VIRTIO_MMIO_QUEUE_DESC_HIGH, (uint32_t)(desc_pa  >> 32));
    mmio_w32(base, VIRTIO_MMIO_QUEUE_AVAIL_LOW, (uint32_t)(avail_pa));
    mmio_w32(base, VIRTIO_MMIO_QUEUE_AVAIL_HIGH,(uint32_t)(avail_pa >> 32));
    mmio_w32(base, VIRTIO_MMIO_QUEUE_USED_LOW,  (uint32_t)(used_pa));
    mmio_w32(base, VIRTIO_MMIO_QUEUE_USED_HIGH, (uint32_t)(used_pa  >> 32));
    mmio_w32(base, VIRTIO_MMIO_QUEUE_READY, 1);

    /* 7. DRIVER_OK */
    status |= VIRTIO_STATUS_DRIVER_OK;
    mmio_w32(base, VIRTIO_MMIO_STATUS, status);

    /* 8. Create host resource, attach backing, set scanout */
    gpu_cmd_resource_create_2d();
    gpu_cmd_attach_backing();
    gpu_cmd_set_scanout();

    /* Initial flush so the display goes black (not garbage) */
    gpu_flush_rect(0, 0, VGPU_WIDTH, VGPU_HEIGHT);

    s_ready = 1;
}

/**
 * virtio_gpu_write_char — write one character to the text console and flush
 * only the affected cell row to the display.
 */
void virtio_gpu_write_char(char c)
{
    if (!s_ready) return;

    if (c == '\r') return;

    if (c == '\n') {
        s_col = 0;
        s_row++;
        if (s_row >= VGPU_ROWS) scroll_up();
        /* Flush the entire screen after scroll (cheap on QEMU) */
        gpu_flush_rect(0, 0, VGPU_WIDTH, VGPU_HEIGHT);
        return;
    }

    if (c == '\t') {
        /* Expand tab to next multiple of 4 */
        int spaces = 4 - (s_col % 4);
        for (int i = 0; i < spaces; i++) virtio_gpu_write_char(' ');
        return;
    }

    /* Render the glyph */
    paint_char_at(s_col, s_row, c);
    /* Flush just this character cell */
    gpu_flush_rect((uint32_t)(s_col * VGPU_CW), (uint32_t)(s_row * VGPU_CH),
                   VGPU_CW, VGPU_CH);

    s_col++;
    if (s_col >= VGPU_COLS) {
        s_col = 0;
        s_row++;
        if (s_row >= VGPU_ROWS) scroll_up();
    }
}

/**
 * hal_console_display_write — strong override of the weak HAL stub.
 * Called by hal_serial_write() on every serial write to mirror output
 * to the VirtIO GPU framebuffer (QEMU graphical window).
 */
void hal_console_display_write(const char *s)
{
    if (!s || !s_ready) return;
    while (*s) {
        virtio_gpu_write_char(*s++);
    }
}
