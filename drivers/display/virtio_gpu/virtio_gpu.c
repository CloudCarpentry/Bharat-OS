/* Submit a two-descriptor chain: [host-readable req] → [device-writable resp].
 * Blocks (polls) until the device marks the chain as used.
 */
static void vq_submit_poll(void *req, uint32_t req_len,
                            void *resp, uint32_t resp_len)
{
    /* Descriptor 0: request (host-readable) */
    s_desc[0].addr  = (uint64_t)(uintptr_t)req;
    s_desc[0].len   = req_len;
    s_desc[0].flags = VIRTQ_DESC_F_NEXT;
    s_desc[0].next  = 1;

    /* Descriptor 1: response (device-writable) */
    s_desc[1].addr  = (uint64_t)(uintptr_t)resp;
    s_desc[1].len   = resp_len;
    s_desc[1].flags = VIRTQ_DESC_F_WRITE;
    s_desc[1].next  = 0;

    /* Put head (desc 0) in the available ring */
    s_avail.ring[s_avail.idx % VQ_SIZE] = 0;
    mb();
    s_avail.idx++;
    mb();

    /* Notify device */
    mmio_w32(s_gpu_base, VIRTIO_MMIO_QUEUE_NOTIFY, CONTROLQ);

    /* Poll until used ring advances */
    while (s_used.idx == s_last_used_idx) {
        mb();
    }
    s_last_used_idx = s_used.idx;
}

