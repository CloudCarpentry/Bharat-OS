static void gpu_cmd_resource_create_2d(void)
{
    static virtio_gpu_resource_create_2d_t req;
    static virtio_gpu_resp_nodata_t        resp;

    req.hdr.type    = VIRTIO_GPU_CMD_RESOURCE_CREATE_2D;
    req.hdr.flags   = 0;
    req.hdr.fence_id = 0;
    req.hdr.ctx_id  = 0;
    req.hdr.padding = 0;
    req.resource_id = VGPU_RESOURCE_ID;
    req.format      = VIRTIO_GPU_FORMAT_B8G8R8X8_UNORM;
    req.width       = VGPU_WIDTH;
    req.height      = VGPU_HEIGHT;

    vq_submit_poll(&req, sizeof(req), &resp, sizeof(resp));
}

static void gpu_cmd_attach_backing(void)
{
    /* Pack attach_backing header + one mem_entry into a single structure */
    struct __attribute__((packed)) {
        virtio_gpu_resource_attach_backing_t hdr;
        virtio_gpu_mem_entry_t               entry;
    } static req;
    static virtio_gpu_resp_nodata_t resp;

    req.hdr.hdr.type    = VIRTIO_GPU_CMD_RESOURCE_ATTACH_BACKING;
    req.hdr.hdr.flags   = 0;
    req.hdr.hdr.fence_id = 0;
    req.hdr.hdr.ctx_id  = 0;
    req.hdr.hdr.padding = 0;
    req.hdr.resource_id = VGPU_RESOURCE_ID;
    req.hdr.nr_entries  = 1;
    req.entry.addr      = (uint64_t)(uintptr_t)s_fb;
    req.entry.length    = sizeof(s_fb);
    req.entry.padding   = 0;

    vq_submit_poll(&req, sizeof(req), &resp, sizeof(resp));
}

static void gpu_cmd_set_scanout(void)
{
    static virtio_gpu_set_scanout_t req;
    static virtio_gpu_resp_nodata_t resp;

    req.hdr.type    = VIRTIO_GPU_CMD_SET_SCANOUT;
    req.hdr.flags   = 0;
    req.hdr.fence_id = 0;
    req.hdr.ctx_id  = 0;
    req.hdr.padding = 0;
    req.r.x         = 0;
    req.r.y         = 0;
    req.r.width     = VGPU_WIDTH;
    req.r.height    = VGPU_HEIGHT;
    req.scanout_id  = 0;
    req.resource_id = VGPU_RESOURCE_ID;

    vq_submit_poll(&req, sizeof(req), &resp, sizeof(resp));
}

static void gpu_flush_rect(uint32_t x, uint32_t y, uint32_t w, uint32_t h)
{
    static virtio_gpu_transfer_to_host_2d_t treq;
    static virtio_gpu_resource_flush_t      freq;
    static virtio_gpu_resp_nodata_t         resp;

    /* Transfer from guest framebuffer to host */
    treq.hdr.type    = VIRTIO_GPU_CMD_TRANSFER_TO_HOST_2D;
    treq.hdr.flags   = 0;
    treq.hdr.fence_id = 0;
    treq.hdr.ctx_id  = 0;
    treq.hdr.padding = 0;
    treq.r.x        = x;
    treq.r.y        = y;
    treq.r.width    = w;
    treq.r.height   = h;
    treq.offset     = (uint64_t)(y * VGPU_WIDTH + x) * 4u;
    treq.resource_id = VGPU_RESOURCE_ID;
    treq.padding    = 0;
    vq_submit_poll(&treq, sizeof(treq), &resp, sizeof(resp));

    /* Flush to display */
    freq.hdr.type    = VIRTIO_GPU_CMD_RESOURCE_FLUSH;
    freq.hdr.flags   = 0;
    freq.hdr.fence_id = 0;
    freq.hdr.ctx_id  = 0;
    freq.hdr.padding = 0;
    freq.r.x        = x;
    freq.r.y        = y;
    freq.r.width    = w;
    freq.r.height   = h;
    freq.resource_id = VGPU_RESOURCE_ID;
    freq.padding    = 0;
    vq_submit_poll(&freq, sizeof(freq), &resp, sizeof(resp));
}

/* -----------------------------------------------------------------------
 * Text console helpers
 * ----------------------------------------------------------------------- */

static void paint_char_at(int col, int row, char c)
{
    int ci = (unsigned char)c;
    if (ci < 32 || ci > 127) ci = '?';
    const uint8_t *glyph = s_font[ci - 32];

    int px = col * VGPU_CW;
    int py = row * VGPU_CH;

    for (int gy = 0; gy < VGPU_CH; gy++) {
        uint8_t bits = glyph[gy];
        for (int gx = 0; gx < VGPU_CW; gx++) {
            uint32_t color = (bits & (0x80u >> gx)) ? COLOR_FG : COLOR_BG;
            s_fb[py + gy][px + gx] = color;
        }
    }
}

static void scroll_up(void)
{
    /* Move rows 1..VGPU_ROWS-1 up by one */
    for (int y = 0; y < (VGPU_ROWS - 1) * VGPU_CH; y++) {
        for (int x = 0; x < VGPU_WIDTH; x++) {
            s_fb[y][x] = s_fb[y + VGPU_CH][x];
        }
    }
    /* Clear bottom row */
    for (int y = (VGPU_ROWS - 1) * VGPU_CH; y < VGPU_HEIGHT; y++) {
        for (int x = 0; x < VGPU_WIDTH; x++) {
            s_fb[y][x] = COLOR_BG;
        }
    }
    s_row = VGPU_ROWS - 1;
}

