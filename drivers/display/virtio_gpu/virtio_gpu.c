/* -----------------------------------------------------------------------
 * QEMU virt MMIO range: 8 slots, each 0x1000 wide, starting at 0x10001000
 * ----------------------------------------------------------------------- */
#define VIRT_MMIO_BASE   0x10001000ULL
#define VIRT_MMIO_STRIDE 0x1000ULL
#define VIRT_MMIO_COUNT  8

/* -----------------------------------------------------------------------
 * Compiler barrier  (no caches on QEMU virt MMIO)
 * ----------------------------------------------------------------------- */
#define mb()  __asm__ volatile("" ::: "memory")

/* -----------------------------------------------------------------------
 * MMIO accessors
 * ----------------------------------------------------------------------- */
static inline void mmio_w32(uintptr_t base, uint32_t off, uint32_t v)
{
    mb();
    *(volatile uint32_t *)(base + off) = v;
    mb();
}
static inline uint32_t mmio_r32(uintptr_t base, uint32_t off)
{
    mb();
    return *(volatile uint32_t *)(base + off);
}

