/*
 * boot_gui.c — Early boot framebuffer renderer
 *
 * Provides minimal pixel-level drawing for the boot splash:
 *   - fill_rect   : solid colour rectangle (used for background/panels)
 *   - draw_pixel  : single pixel write (used by text glyph renderer)
 *   - draw_text   : ASCII text using an embedded 8×16 bitmap font
 *
 * Design constraints (KEEP these, do NOT relax):
 *   1. No malloc, no slab, no page allocator — static state only.
 *   2. No kernel.h, no scheduling, no IPC.
 *   3. Callable before mm_vmm_init() — uses identity-mapped physical address.
 *   4. Safe to call even if boot_gui_run() returns -1 (all ops are no-ops).
 *   5. BHARAT_BOOT_GUI guards the whole translation unit in CMakeLists.
 *
 * Pixel format assumption:
 *   We currently support ARGB8888 / XRGB8888 (4 bytes/pixel) only.
 *   RGB565 (2 bytes/pixel) is detected and handled with a conversion.
 *   Other formats fall back to no-op drawing.
 */

